    void iniciarSesion(){
        String user, pass, usuario="admin", contrasenia="123456";
        user = txtUser.getText();
        pass = String.valueOf(txtPass.getPassword());
        if(user.equals(usuario)&&pass.equals(contrasenia)){
            new Principal().setVisible(true);
            this.dispose();
        }else
            JOptionPane.showMessageDialog(null, "Su usuario o contraseña son incorrectos","Mensaje",JOptionPane.ERROR_MESSAGE);
    }


    private void llenarArregloString(){
        String[] paises = {"México","Perú","Bolivia","Colombia"};
        for (String pais : paises) {
            jComboBox2.addItem(pais);
        }
    }
    
    private void llenarArregloObjetos(){
        ArrayList<Pais> listaPaises = new ArrayList<>();
        Pais mexico = new Pais(1, "México");
        Pais peru = new Pais(1, "Perú");
        Pais bolivia = new Pais(1, "Bolivia");
        Pais colombia = new Pais(1, "Colombia");
        listaPaises.add(mexico);
        listaPaises.add(peru);
        listaPaises.add(bolivia);
        listaPaises.add(colombia);
        
        for (Pais p : listaPaises) {
            jComboBox3.addItem(p);
        }
    }
        

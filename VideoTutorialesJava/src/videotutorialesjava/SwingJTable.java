    private void setModelo(){
        String[] cabecera = {"NÂ°","ID","Nombre"};
        dtmPaises.setColumnIdentifiers(cabecera);
        tblPaises.setModel(dtmPaises);
    }
    
    private void llenarLista(){
        String id = txtId.getText();
        String nombre = txtNombre.getText();
        Pais pais = new Pais(Integer.parseInt(id), nombre);
        listaPaises.add(pais);
    }
    
    private void setDatos(){
        Object[] datos = new Object[dtmPaises.getColumnCount()];
        int i=1;
        dtmPaises.setRowCount(0);
        for (Pais pais : listaPaises) {
            datos[0] = i;
            datos[1] = pais.getId();
            datos[2] = pais.getNombre();
            i++;
            dtmPaises.addRow(datos);            
        }
        tblPaises.setModel(dtmPaises);
    }
    
